#include "main.h"
#include <string.h>

/**
 * _strcat - function that appends src string to dest sting
 * @src: source string
 * @dest: destination string
 *
 * Return: pointer to resulting dest string
*/
char *_strcat(char *dest, char *src)
{
	int i;

	for (i = 0; i <= src[i]; i++)
	{
		dest[i + i] = src[i];
		dest[i + i] = '0';
	}
	return (dest);
}
